package beans;

public class Rom {
    private String romID;
    private int etasje;
    private int tilgangniva;

    public String getRomID() {
        return romID;
    }

    public void setRomID(String romID) {
        this.romID = romID;
    }

    public int getEtasje() {
        return etasje;
    }

    public void setEtasje(int etasje) {
        this.etasje = etasje;
    }

    public int getTilgangniva() {
        return tilgangniva;
    }

    public void setTilgangniva(int tilgangniva) {
        this.tilgangniva = tilgangniva;
    }
}
